#include "../includes/libft.h"
#include "../includes/push_swap.h"

static int	num_len(int n)
{
	int	result;

	result = 0;
	if (n <= 0)
	{
		result = 1;
		n = n * -1;
	}
	while (n != 0)
	{
		n = n / 10;
		result++;
	}
	return (result);
}

char	*ft_itoa(int n)
{
	char	*str;
	int		i;

	if (n == 0)
		return (ft_strdup("0"));
	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	str = (char *) malloc(num_len(n) + 1);
	i = num_len(n) - 1;
	if (!str)
		return (NULL);
	str[i + 1] = '\0';
	if (n < 0)
	{
		str[0] = '-';
		n = n * -1;
	}
	while (n > 0)
	{
		str[i] = (n % 10) + '0';
		n = n / 10;
		i--;
	}
	return (str);
}
